function TableTemplate($LocID){
    $root = $_SERVER['DOCUMENT_ROOT']."/UBRepair/";
    include_once $root . "database/Database.php";
    $view = viewFacility($LocID);
    while($row = mysqli_fetch_assoc($view))
    {
        $loc = mysqli_fetch_assoc(getLocation($row['LocID']));
        echo"
            <tr>
                <td>  <input type='text' name='ID' size='9' value='".$row['FacID']."' readonly></td>
                <td>  <input type='text' name='itemName' size='15' value='".$row['Name']."'readonly></td>
                <td>  <input type='text' name='itemSerial' size='15' value='".$row['Serial']."'readonly></td>
                <td>  <input type='text' name='itemWarranty' size='15' value='".$row['Warranty']."'readonly></td>
                <td>  <input type='text' name='itemLoc' size='24' value='".$loc['Location']."'readonly></td>
                <td>".$row['Status']."</td>
                
            </tr>
        ";
    }
}
